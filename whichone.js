// Global variables for background images
let leftMiddleBackground = '';
let rightMiddleBackground = '';

// Function to get random background images
function getRandomBackgrounds() {
    const randomIndex1 = Math.floor(Math.random() * columnBackgrounds.length);
    let randomIndex2 = Math.floor(Math.random() * columnBackgrounds.length);
    
    // Ensure we don't get the same image twice
    while (randomIndex2 === randomIndex1) {
        randomIndex2 = Math.floor(Math.random() * columnBackgrounds.length);
    }
    
    leftMiddleBackground = columnBackgrounds[randomIndex1];
    rightMiddleBackground = columnBackgrounds[randomIndex2];
}

// Function to create text links
function createTextLinks(links, containerSelector, linkClass) {
    const container = document.querySelector(containerSelector);
    
    // Double the links to create seamless loop
    const doubledLinks = [...links, ...links];
    
    doubledLinks.forEach((link, index) => {
        const linkElement = document.createElement('a');
        linkElement.href = link.url;
        linkElement.target = '_blank';
        linkElement.rel = 'noopener noreferrer';
        linkElement.textContent = link.text;
        linkElement.className = `text-link ${linkClass}`;
        
        // Set height for equal distribution
        linkElement.style.height = `${100 / doubledLinks.length}%`;
        
        container.appendChild(linkElement);
    });
}

// Function to create background image divs
function createBackgroundImages(backgroundImage, containerSelector) {
    const container = document.querySelector(containerSelector);
    
    // Create 8 background divs to create seamless loop
    for (let i = 0; i < 8; i++) {
        const bgDiv = document.createElement('div');
        bgDiv.className = 'background-image';
        bgDiv.style.backgroundImage = `url(${backgroundImage})`;
        container.appendChild(bgDiv);
    }
}

// Function to initialize the page
function initializePage() {
    // Get random background images
    getRandomBackgrounds();
    
    // Create left column links
    createTextLinks(leftColumnLinks, '#left-links-container', 'left-link');
    
    // Create right column links
    createTextLinks(rightColumnLinks, '#right-links-container', 'right-link');
    
    // Create background images for left column
    createBackgroundImages(leftMiddleBackground, '#left-image-container');
    
    // Create background images for right column
    createBackgroundImages(rightMiddleBackground, '#right-image-container');
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initializePage);

